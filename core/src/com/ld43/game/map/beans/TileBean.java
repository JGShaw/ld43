package com.ld43.game.map.beans;

public class TileBean {

    private Integer rot;
    private Integer y;
    private Boolean flipX;
    private Integer index;
    private Integer tile;
    private Integer x;

    public Integer getRot() {
        return rot;
    }

    public void setRot(Integer rot) {
        this.rot = rot;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }

    public Boolean getFlipX() {
        return flipX;
    }

    public void setFlipX(Boolean flipX) {
        this.flipX = flipX;
    }

    public Integer getIndex() {
        return index;
    }

    public void setIndex(Integer index) {
        this.index = index;
    }

    public Integer getTile() {
        return tile;
    }

    public void setTile(Integer tile) {
        this.tile = tile;
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

}